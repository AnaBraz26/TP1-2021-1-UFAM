    // Nesse método é decidido quais colunas irão aparecer na tabela
    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) jTGarçons.getModel();
        modelo.setNumRows(0);
        GarçomDAO gdao = new GarçomDAO();
        
        for(Garçom g: gdao.read()){
            modelo.addRow(new Object[]{g.getId(),g.getNome(),g.getSalario()});
        }
    }

