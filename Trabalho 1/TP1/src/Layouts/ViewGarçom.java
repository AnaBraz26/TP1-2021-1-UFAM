    //Nesse método é feita a exclusão do garçom após selecionada a linha
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(jTGarçons.getSelectedRow() != -1){
            Garçom g = new Garçom();
            GarçomDAO dao = new GarçomDAO();
            
            g.setId((int)jTGarçons.getValueAt(jTGarçons.getSelectedRow(), 0));
            dao.delete(g); 

            txtName.setText("");
            txtSalario.setText("");

            readJtable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

    
    //Os próximos 2 métodos servem para selecionar a linha que se deseja para alteração ou exclusão
    private void jTGarçonsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTGarçonsMouseClicked
            if(jTGarçons.getSelectedRow() != -1){
                txtName.setText(jTGarçons.getValueAt(jTGarçons.getSelectedRow(), 1).toString());
                txtSalario.setText(jTGarçons.getValueAt(jTGarçons.getSelectedRow(), 2).toString());
            }
    }//GEN-LAST:event_jTGarçonsMouseClicked

    private void jTGarçonsKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTGarçonsKeyReleased
       if(jTGarçons.getSelectedRow() != -1){
            txtName.setText(jTGarçons.getValueAt(jTGarçons.getSelectedRow(), 1).toString());
            txtSalario.setText(jTGarçons.getValueAt(jTGarçons.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_jTGarçonsKeyReleased

    
    //Nesse método é feito a alteração de alguma informação sobre o garçom, após selecionado na tabela da interface 
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTGarçons.getSelectedRow() != -1){
            Garçom g = new Garçom();
            GarçomDAO dao = new GarçomDAO();
            g.setNome(txtName.getText());
            g.setSalario(Double.parseDouble(txtSalario.getText()));
            g.setId((int)jTGarçons.getValueAt(jTGarçons.getSelectedRow(), 0));
            dao.update(g); 

            txtName.setText("");
            txtSalario.setText("");

            readJtable();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

