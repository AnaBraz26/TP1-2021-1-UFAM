    // Nesse método é decidido quais colunas irão aparecer na tabela
      public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) jTComidas.getModel();
        modelo.setNumRows(0);
        ComidasDAO cdao = new ComidasDAO();
        
        for(Comidas c: cdao.read()){
            modelo.addRow(new Object[]{c.getId(),c.getNome(),c.getIngredientes(),c.getValor()});
        }
    }
