    //Nesse métdo é feita a alteração após selecionada a linha
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTComidas.getSelectedRow() != -1){
            Comidas c = new Comidas();
            ComidasDAO dao = new ComidasDAO();
            c.setNome(txtNome.getText());
            c.setIngredientes(txtIngredientes.getText());
            c.setValor(Double.parseDouble(txtValor.getText()));
            c.setId((int)jTComidas.getValueAt(jTComidas.getSelectedRow(), 0));
            dao.update(c);

            txtNome.setText("");
            txtIngredientes.setText("");
            txtValor.setText("");
            
            readJtable();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    
    //Nesse método é feita a exclusão após selecionada a linha
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(jTComidas.getSelectedRow() != -1){
            Comidas c = new Comidas();
            ComidasDAO dao = new ComidasDAO();

            c.setId((int)jTComidas.getValueAt(jTComidas.getSelectedRow(), 0));
            dao.delete(c);

            txtNome.setText("");
            txtIngredientes.setText("");
            txtValor.setText("");

            readJtable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

