    // Nesse método é decidido quais colunas irão aparecer na tabela
    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) jTMesa.getModel();
        modelo.setNumRows(0);
        MesasDAO mdao = new MesasDAO();
        
        for(Mesas m: mdao.read()){
            modelo.addRow(new Object[]{m.getId(), m.getNumero(), m.getIdPedidos().getId() ,m.getIdGarçom().getId()});
        }
    }
