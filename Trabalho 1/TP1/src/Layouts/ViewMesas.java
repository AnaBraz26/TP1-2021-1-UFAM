    //Nesse método é cadastrada o número da mesa, o id pedido feito pela mesa e o id garçom que atendeu
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Mesas m = new Mesas();
        MesasDAO dao = new MesasDAO();
        
        m.setNumero(Integer.parseInt(txtMesa.getText()));
        
        Pedidos p = new Pedidos();
        p.setId(Integer.parseInt(txtPedido.getText()));        
        m.setIdPedidos(p);
        
        Garçom g = new Garçom();
        g.setId(Integer.parseInt(txtGarçom.getText()));
        m.setIdGarçom(g);
            
        dao.create(m);

        txtMesa.setText("");
        txtPedido.setText("");
        txtGarçom.setText("");
        
        readJtable();
    }//GEN-LAST:event_jButton3ActionPerformed

