    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTMesa.getSelectedRow() != -1){
            Mesas m = new Mesas();
            MesasDAO dao = new MesasDAO();
            m.setNumero(Integer.parseInt(txtMesa.getText()));      
            
            Pedidos p = new Pedidos();
            p.setId(Integer.parseInt(txtPedido.getText()));        
            m.setIdPedidos(p);

            Garçom g = new Garçom();
            g.setId(Integer.parseInt(txtGarçom.getText()));
            m.setIdGarçom(g); 
        
            m.setId((int)jTMesa.getValueAt(jTMesa.getSelectedRow(), 0));
            dao.update(m);
            
            txtMesa.setText("");
            txtPedido.setText("");
            txtGarçom.setText("");
            readJtable();
        }
      
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(jTMesa.getSelectedRow() != -1){
            Mesas m = new Mesas();
            MesasDAO dao = new MesasDAO();
            
            m.setId((int)jTMesa.getValueAt(jTMesa.getSelectedRow(), 0));
            dao.delete(m); 

            txtMesa.setText("");
            txtPedido.setText("");
            txtGarçom.setText("");

            readJtable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jTMesaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTMesaKeyReleased
        if(jTMesa.getSelectedRow() != -1){
            txtMesa.setText(jTMesa.getValueAt(jTMesa.getSelectedRow(), 1).toString());
            txtPedido.setText(jTMesa.getValueAt(jTMesa.getSelectedRow(), 2).toString());
            txtGarçom.setText(jTMesa.getValueAt(jTMesa.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_jTMesaKeyReleased

