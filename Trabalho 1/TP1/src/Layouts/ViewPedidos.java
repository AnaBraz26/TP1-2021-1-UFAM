    // Nesse método é decidido quais colunas irão aparecer na tabela
    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) jTPedidos.getModel();
        modelo.setNumRows(0);
        PedidosDAO pdao = new PedidosDAO();
        
        for(Pedidos p: pdao.read()){
            modelo.addRow(new Object[]{p.getId(),p.getIdComidas().getId(),p.getStatus(), p.getObservação()});
        }
    }
