    //Nesse método, deve-se selecionar a linha desejada e fazer a alteração na escrita, após o clicar o botão a alteração é realizada
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         if(jTPedidos.getSelectedRow() != -1){
            Pedidos p = new Pedidos();
            PedidosDAO dao = new PedidosDAO();
            
            Comidas comidas = new Comidas();
            comidas.setId(Integer.parseInt(txtComida.getText()));
        
            p.setIdComidas(comidas);
            p.setStatus(txtStatus.getText());
            p.setObservação(txtObs.getText());
            p.setId((int)jTPedidos.getValueAt(jTPedidos.getSelectedRow(), 0));
            dao.update(p);

            txtComida.setText("");
            txtObs.setText("");
            txtStatus.setText("");

            readJtable();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    //Nesse método é feita exclusão da linha após selecionada e acionado o botão
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if(jTPedidos.getSelectedRow() != -1){
            Pedidos p = new Pedidos();
            PedidosDAO dao = new PedidosDAO();

            p.setId((int)jTPedidos.getValueAt(jTPedidos.getSelectedRow(), 0));
            dao.delete(p);

            txtComida.setText("");
            txtObs.setText("");
            txtStatus.setText("");

            readJtable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    //Os próximos 2 métodos servem para selecionar a linha que se deseja para alteração ou exclusão
    private void jTPedidosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTPedidosKeyReleased
       if(jTPedidos.getSelectedRow() != -1){
                txtComida.setText(jTPedidos.getValueAt(jTPedidos.getSelectedRow(), 1).toString());
                txtStatus.setText(jTPedidos.getValueAt(jTPedidos.getSelectedRow(), 2).toString());
                txtObs.setText(jTPedidos.getValueAt(jTPedidos.getSelectedRow(), 3).toString());
            }
    }//GEN-LAST:event_jTPedidosKeyReleased

