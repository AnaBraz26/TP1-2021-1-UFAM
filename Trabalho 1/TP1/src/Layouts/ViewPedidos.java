    //Nesse método ao clicar no botão, o pedido é feito, ou seja, cadastrado no sistema
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Pedidos p = new Pedidos();
        PedidosDAO dao = new PedidosDAO();
        
        Comidas comidas = new Comidas();
        comidas.setId(Integer.parseInt(txtComida.getText()));
        
        p.setIdComidas(comidas);
        p.setStatus(txtStatus.getText());
        p.setObservação(txtObs.getText());
        dao.create(p);

        txtComida.setText("");
        txtObs.setText("");
        txtStatus.setText("");
        
        readJtable();
    }//GEN-LAST:event_jButton1ActionPerformed

